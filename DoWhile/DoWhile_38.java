package While;

public class DoWhile_38 {
    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println("Numeros impares ate 20: " + i);
            i += 2;  // Incrementa 2 para obter o próximo número impar.
        } while (i <= 20);

    }
}
