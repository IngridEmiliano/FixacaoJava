package Expressões_Ariméticas;

public class Expressôes_Ariméticas14 {
    public static void main(String[] args) {
        double resultadoSubtracao = 15 - 5;
        double resultadoPow =Math.pow(3, 2);

        double resultadoFinal = resultadoPow * resultadoSubtracao;
        System.out.println("O resultado de (15 - 5) * (3^2) é: " + resultadoFinal);
    }
}
