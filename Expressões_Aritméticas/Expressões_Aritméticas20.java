package Expressões_Ariméticas;

public class Expressôes_Ariméticas20 {
    public static void main(String[] args) {

        double resultadoAdicao = (10 + 5);
        double resultadoPow = Math.pow(2, 2);

        double resultadoFinal = resultadoPow / resultadoAdicao;
        System.out.println("O resultado de (10 + 5) / 2 elevado por 2 é: " + resultadoFinal);
    }
}


